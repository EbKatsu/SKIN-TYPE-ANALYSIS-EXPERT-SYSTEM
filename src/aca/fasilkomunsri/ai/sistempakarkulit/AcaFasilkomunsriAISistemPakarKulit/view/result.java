    private void showActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showActionPerformed
        // TODO add your handling code here:
        namaUser.setText(Nama);
        jkUser.setText(JK);
        pNormal.setText(String.valueOf(gkNormal) + "%");
        pMinyak.setText(String.valueOf(gkBerminyak) + "%");
        pKering.setText(String.valueOf(gkKering) + "%");
        pKombinasi.setText(String.valueOf(gkKombinasi) + "%");
        pSensitif.setText(String.valueOf(gkSensitif) + "%");
        
        if (gkNormal > gkBerminyak && gkNormal > gkKering && gkNormal > gkKombinasi && gkNormal > gkSensitif) {
            jKulit.setText("Kulit Normal");
            solusi.setText("1. Membersihkan  wajah cukup dengan air, ketika kulit wajah dalam keadaan  tanpa make up\n2. Jika kulit wajah dalam keadaan bermakeup, bisa dibersihkan menggunakan milk cleanser, face tonic dan facial foam.\n3. Bisa menggunakan face tonic dan krim pelembab, ketika musim panas. Karena di musim panas kulit normal akan terasa agak kering\n4. Perawatan facial di klinik kecantikan diperlukan sewaktu-waktu saja, cukup 1 kali dalam 3 bulan \n5. Menggunakan krim tabir surya untuk melindungi dari panas sinar matahari");
        }
        else if (gkBerminyak > gkNormal && gkBerminyak > gkKering && gkBerminyak > gkKombinasi && gkBerminyak > gkSensitif) {
            jKulit.setText("Kulit Berminyak");
            solusi.setText("1. Membersihkan wajah menggunakan  facial foam, kemudian dibilas sampai bersih\n2. Setelah mencuci wajah, gunakan face tonic ");
        }
        else if (gkKering > gkNormal && gkKering > gkBerminyak && gkKering > gkKombinasi && gkKering > gkSensitif) {
            jKulit.setText("Kulit Kering");
            solusi.setText("1. Gunakan pelembab sesering mungkin, baik siang maupun malam.\n" +
"2. Gunakan tabir surya pada siang hari, karena kulit kering ini sangat mudah terkena flek kecokelatan\n" +
"3. Jangan terlalu sering menggunakan sabun wajah");
        }
        else if (gkKombinasi > gkNormal && gkKombinasi > gkBerminyak && gkKombinasi > gkKering && gkKombinasi > gkSensitif) {
            jKulit.setText("Kulit Kombinasi");
            solusi.setText("1. Gunakan selalu facial foam, milk cleanser dan face tonic\n2. Lakukan perawatan facial di salon kecantikan sebulan sekali\n3. Oleskan tipis-tipis krim atau lotion pencegah komedo pada malam hari.");
        }
        else if (gkSensitif > gkNormal && gkSensitif > gkBerminyak && gkSensitif > gkKering && gkSensitif > gkKombinasi) {
            jKulit.setText("Kulit Sensitif");
            solusi.setText("1.  Memilih produk yang ramah untuk kulit\n" +
"2. Menjauhi bahan iritatif\n" +
"3. Selalu memakai pelembab yang cocok untuk kulit sensitif\n" +
"4. Batasi waktu mandi\n" +
"5. Batasi paparan sinar matahari dan gunakan tabir surya");
        }
    }//GEN-LAST:event_showActionPerformed

